"use client";

import { useState } from "react";

interface Step {
  id: number;
  question: string;
  options?: string[];
  type: 'select' | 'text' | 'multi';
}

const steps: Step[] = [
  { id: 1, question: "What's your industry or main business area?", type: 'text' },
  { id: 2, question: "Rough company size (revenue or team)?", options: ["Under $2M / <10 people", "$2M–$10M / 10-50 people", "$10M+ / 50+ people"], type: 'select' },
  { id: 3, question: "Biggest AI-related pain right now?", options: ["Too many tools, no clear wins", "Operations are manual and slow", "Marketing/leads not scaling", "Team overwhelmed by hype vs. results"], type: 'multi' },
  { id: 4, question: "Timeline for seeing real impact?", options: ["Next 30-60 days", "Next quarter", "6+ months"], type: 'select' },
];

export default function PlanningWizard() {
  const [currentStep, setCurrentStep] = useState(1);
  const [answers, setAnswers] = useState<Record<number, string | string[]>>({});
  const [generatedPlan, setGeneratedPlan] = useState<string | null>(null);
  const [isGenerating, setIsGenerating] = useState(false);

  const current = steps.find(s => s.id === currentStep)!;

  const handleAnswer = (value: string) => {
    setAnswers(prev => {
      if (current.type === 'multi') {
        const currentVals = (prev[current.id] as string[]) || [];
        const newVals = currentVals.includes(value) 
          ? currentVals.filter(v => v !== value) 
          : [...currentVals, value];
        return { ...prev, [current.id]: newVals };
      }
      return { ...prev, [current.id]: value };
    });
  };

  const next = () => {
    if (currentStep < steps.length) {
      setCurrentStep(currentStep + 1);
    } else {
      generatePlan();
    }
  };

  const prev = () => {
    if (currentStep > 1) setCurrentStep(currentStep - 1);
  };

  const generatePlan = () => {
    setIsGenerating(true);
    
    // Simulate the "skilled planner" system doing the work
    setTimeout(() => {
      const plan = `
**Initial AI Plan generated by the betterwithai System** (starting with this website)

**Based on your inputs:**
- Industry: ${answers[1] || 'Not specified'}
- Size: ${answers[2] || 'Not specified'}
- Top pains: ${(answers[3] as string[] || []).join(', ') || 'Not specified'}
- Timeline: ${answers[4] || 'Not specified'}

**Prioritized Opportunities (ROI-ranked by our system):**
1. **Quick Win Automation** (High ROI, low effort): [Tailored to your pains — e.g. lead qualification or ops dispatch if relevant]. Estimated 15-25 hrs/week back + clear revenue impact.
2. **Process Intelligence Layer**: Use AI to surface hidden patterns in your current tools/data.
3. **Client-Facing AI**: Personalized experiences or self-serve tools to scale without headcount.

**Discussion Points the System Would Raise (skilled planner style):**
- What does "success" actually look like in 90 days for you?
- Budget range and decision-makers involved?
- Any existing tech stack we must integrate with?
- Risk tolerance for quick experiments vs. big builds?

**Recommended Next Step:**
This is a sample. Buy the full AI Roadmap for the complete, audited version with your real data + 60-min discussion call.

Or let the system escalate: We'll auto-generate a scoped proposal.

[Buy AI Roadmap] or [Discuss with the team]

*This website + Ops Leader agent system did 80% of the initial planning and discussion. Humans + agents handle the rest with full context.*
      `;
      setGeneratedPlan(plan);
      setIsGenerating(false);
    }, 1200);
  };

  const reset = () => {
    setCurrentStep(1);
    setAnswers({});
    setGeneratedPlan(null);
  };

  if (generatedPlan) {
    return (
      <div className="card p-8">
        <h3 className="text-2xl font-semibold mb-4">Your Initial Plan from the System</h3>
        <pre className="whitespace-pre-wrap text-sm bg-black/30 p-6 rounded-lg text-white/90 font-mono leading-relaxed">{generatedPlan}</pre>
        
        <div className="flex flex-col sm:flex-row gap-4 mt-8">
          <a 
            href="https://buy.stripe.com/test_YOUR_PLANNING_LINK" 
            target="_blank" 
            className="btn-primary flex-1 justify-center text-lg py-4"
            onClick={() => alert('Tesla mode: Stripe opens with your exact plan pre-filled + terms accepted. One tap and you\'re done.')}
          >
            Order Now — $2,497
          </a>
          <button onClick={reset} className="btn-secondary flex-1 justify-center">Adjust</button>
        </div>
        <p className="mt-4 text-xs text-center text-white/50">
          Configure here → Pay once → Your AI is live in the portal instantly.<br />No sales calls. No paperwork. No waiting.
        </p>
      </div>
    );
  }

  const currentAnswer = answers[current.id];
  const hasAnswer = current.type === 'multi' 
    ? (currentAnswer as string[] || []).length > 0 
    : !!currentAnswer;

  return (
    <div className="card p-8">
      <div className="flex justify-between items-center mb-6">
        <div>
          <span className="text-[#C6FF3A] text-sm">STEP {currentStep} of {steps.length}</span>
          <h3 className="text-2xl font-semibold mt-1">{current.question}</h3>
        </div>
        <div className="text-xs text-white/50">The website planner</div>
      </div>

      <div className="space-y-3 mb-8">
        {current.type === 'text' && (
          <input 
            type="text" 
            className="w-full bg-white/5 border border-white/10 p-4 rounded-lg text-white placeholder:text-white/40 focus:outline-none focus:border-[#C6FF3A]"
            placeholder="Type your answer..."
            value={(currentAnswer as string) || ''}
            onChange={(e) => handleAnswer(e.target.value)}
            onKeyDown={(e) => { if (e.key === 'Enter' && hasAnswer) next(); }}
          />
        )}

        {(current.type === 'select' || current.type === 'multi') && current.options?.map(option => {
          const isSelected = current.type === 'multi' 
            ? (currentAnswer as string[] || []).includes(option)
            : currentAnswer === option;
          
          return (
            <button
              key={option}
              onClick={() => handleAnswer(option)}
              className={`w-full text-left p-4 rounded-lg border transition-all ${isSelected 
                ? 'border-[#C6FF3A] bg-[#C6FF3A]/10' 
                : 'border-white/10 hover:border-white/30 bg-white/5'}`}
            >
              {option}
              {current.type === 'multi' && isSelected && ' ✓'}
            </button>
          );
        })}
      </div>

      <div className="flex gap-4">
        {currentStep > 1 && (
          <button onClick={prev} className="btn-secondary flex-1">Back</button>
        )}
        <button 
          onClick={next} 
          disabled={!hasAnswer || isGenerating}
          className="btn-primary flex-1 disabled:opacity-50"
        >
          {currentStep === steps.length ? (isGenerating ? 'Generating plan...' : 'Generate My Initial Plan') : 'Next'}
        </button>
      </div>

      <p className="mt-6 text-xs text-center text-white/50">
        This is the website doing the skilled planning work. Real Roadmap goes much deeper with your actual data.
      </p>
    </div>
  );
}
